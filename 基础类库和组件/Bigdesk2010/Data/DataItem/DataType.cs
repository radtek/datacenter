using System;

namespace Bigdesk2010.Data
{
    /// <summary>
    /// 数据类型
    /// </summary>
    public enum DataType
    {
        /// <summary>
        /// 字符串，如 "hello"
        /// C#类型：String
        /// </summary>
        String = 0,

        /// <summary>
        /// 布尔型
        /// 取值范围 true、false、0、1
        /// C#类型：Boolean
        /// </summary>
        Boolean = 1,

        /// <summary>
        /// 有符号 32 位整数
        /// 取值范围：-2,147,483,648 到 2,147,483,647
        /// C#类型：Int32
        /// </summary>
        Int32 = 2,

        /// <summary>
        /// 有符号 64 位整数
        /// 取值范围：-2^63 到 2^63-1
        /// C#类型：Int64
        /// </summary>
        Int64 = 3,

        /// <summary>
        /// 浮点型，精度是 15 到 16 位
        /// 取值范围：±5.0e-324 到 ±1.7e308
        /// C#类型：Double
        /// </summary>
        Double = 4,

        /// <summary>
        /// 适合于财务和货币计算
        /// 取值范围：28 到 29 位有效位
        /// C#类型：Decimal
        /// </summary>
        Decimal = 5,

        /// <summary>
        /// 日期类型，如 2009-01-01，时间部分固定为00:00:00
        /// 取值范围：0001-01-01 到 9999-12-31
        /// C#类型：DateTime
        /// </summary>
        Date = 6,

        /// <summary>
        /// 时间类型，24时制，如 13:11:00，日期部分固定为2010-01-01
        /// 取值范围：00:00:00 至 23:59:59
        /// C#类型：DateTime
        /// </summary>
        Time = 7,

        /// <summary>
        /// 日期类型，如 2009-01-01 13:11:00
        /// 取值范围：0001-01-01 00:00:00 至 9999-12-31 23:59:59
        /// C#类型：DateTime
        /// </summary>
        DateTime = 8,
    }
}